<h1>FEATURES OF CSS </h1>

<pre >
1- CSS का उपयोग web designing के लिए किया जाता है।

2- यह भाषा styles जैसे (text color, font style, column size layout, background color और images) को कण्ट्रोल करती है।

3- यह भाषा HTML के documents को कण्ट्रोल करती है।

4- यह JavaScript और PHP जैसी भाषाओ को समझने में मदद करती है।

5- इस भाषा को maintain करना आसान है।
</pre>
<h1>APPLICATION OF CSS </h1> 
 <pre>
1- CSS का उपयोग वेबसाइट के elements को maintain करने के लिए किया जाता है।

2- इस भाषा का उपयोग animation बनाने के लिए किया जाता है।

3- यह भाषा वेब पेज पर effect add करने में मदद करती है।

4- इस भाषा का उपयोग HTML के साथ dynamic web template डिज़ाइन करने के लिए किया जाता है।

5- इस भाषा का उपयोग e- commerce platform पर user experience को increase करने के लिए किया जाता है।

6- यह भाषा image को edit और update करने में मदद करती है।

7- यह भाषा वेबसाइट को maintain और manage करके रखती है।

8- यह वेबसाइट के look को परिभाषित करने में मदद करती है।

9- यह भाषा devices के लिए material को optimize करती है।
</pre>
<h1>DISADVANTAGES OF CSS</h1>
<pre>
