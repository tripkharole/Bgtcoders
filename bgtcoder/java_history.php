<h1>History of Java</h1>
<P> 1.Java (जावा) एक object oriented Programming Languageहै। Java का अविष्कार James Gosling तथा उसके साथियों ने सन् 1991 में sun Microsystems में किया था।

<p>2.Java का Real नाम Oak था लेकिन बाद में इसका नाम बदलने की चर्चा की गयी परन्तु कोई नाम तय कर पाना सम्भव ना हो पाया फिर Oak टीम coffee पीने के लिए चली गयी और वही उन्होंने Oak का नाम Java रख दिया।</P>

<p> 3.वैसे तो Java का Use केवल application और Software को Develop करने के लिए किया जाता है परन्तु Internet में सभी प्रकार के कार्यों में भी इसका Use किया जाता है।</P>

<p>4. Java Programming Language को बनाने वाली team को Green Team के नाम से जाना जाता है।</P>
<p>5. शुरुआत मे Java programming language को कुछ digital devices जैसे set top box, और television के लिए बनाया गया था लेकिन Advance Technology होने के कारण यह internet के लिए भी suitable (उपयुक्त) थी। बाद में Java को Netscape के द्वारा खरीद लिया गया।</p>

<p>6.Java को James Gosling  के द्वारा सन 1995 मे develop किया गया था इसलिए James Gosling को Father Of Java के नाम से भी जाना जाता है। इस project को इनकी team के द्वारा 90’s के दशक मे start किया गया था।</p>

<p>7. Starting मे Java का नाम Oak था बाद मे सन 1995 मे इसका नाम Oak से बदलकर Java रख दिया गया। क्योंकि Oak पहले से ही Oak technologies नाम की कंपनी का ट्रेडमार्क था।</p>

<p>8. Java को बनाने के लिए simple, Robust, Portable, platform independent, Secured, High Performance, Architect Neutral, object oriented और dynamic जैसे सिद्धांतों का Use किया गया है।</p>

<p>9. आजकल Java का Use internet programming, और electronic devices मे use किया जा रहा है। James Gosling, Mike Sheridan और Patrick Naughton के द्वारा Java programming language के project को June 1991 मे start किया गया था। इनकी team का नाम Green Team था।</p>
 <p>10. जावा में source Code को byte Code में compiled किया जाता है जबकि अन्य languages में source Code को machine Code में compiled किया जाता है।</p>
