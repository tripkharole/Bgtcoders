<h1>Js Script/Java Script History</h1>
<p>Java Script को Brendan Eich ने 1995 में Develop किया था. तब उस समय वो Netscape कंपनी में काम करते थे. Netscape तब का सबसे प्रसिद्ध browser था.</p>

<p>इस Language को पहले LiveScript कहा जाता था. बहुत सारें ऐसे लोग है जो JavaScript और Java को एक ही language समझते हैं परन्तु ये दोनों अलग-अलग भाषाएँ हैं.</p>

<p>java एक बहुत ही complex language है जबकि  एक scripting लैंग्वेज है.</p>

<h1>JavaScript को कैसे run करते हैं?</h1>
<p>JavaScript खुद से run नहीं होती. इसे run करने के लिए एक browser की आवश्यकता होती है. इसे run करने के लिए यूजर HTML page को request करता है. तब script को browser को भेज दिया जाता है और उसके बाद browser इसे execute करता है.</p>
<h1>JavaScript और java के बीच Difference</h1>
<p>1.यह weakly typed Language है जबकि	 यह strongly typed Language है.</p>
<p>2.यह object पर आधारित Scripting Language है.जबकि	यह object-oriented programming Language है.</p>
<p>3.यह browser पर run होता है जबकि यह JVM (java virtual machine) पर run होता है.</p>
<p>4.इसके objects, prototype पर आधारित है.जबकि Java के objects, class पर आधारित होते हैं.</p>
<p>5.यह multithreading को sport नही करता जबकि यह multithreading को sport करता है.</p>
<p>6.Javascript code आसान तथा complicated नही होता है जबकि Java code  complicated तथा कठिन होता है।</p>
<p>7.Javascript Java के मुकाबले ज्यादा Platform को support करता है।</p>
<p>8.Javascript stand-alone Language नही है इसको html के साथ Use किया जाता है, जबकि Java stand-alone Language  है इसे किसी अन्य Language की आवश्यकता नही है।</p>
