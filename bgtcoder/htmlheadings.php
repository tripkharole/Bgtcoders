        <h1>HTML HEADINGS</h1>
   <p>
   HTML heading या HTML h tag को heading or subheading के रूप में define किया जा सकता है
   जिसे आप webpage पर represent करना चाहते हैं। जब आप text को heading tag  <textarea><h1>.........</h1></textarea>  के भीतर रखते हैं,
   तो यह browser पर bold format में show होता है और text का size heading की number पर depends करता है।
   six  different HTML headings हैं जिन्हें <textarea><h1> से <h6></textarea> tag के साथ define किया गया है,
   highest level h1 (main heading) से minimum level h6 (minimum importent heading)।
   h1 largest heading tag है और h6 smallest  है। इसलिए h1 का use सबसे main heading के लिए किया जाता है 
   और h6 का उपयोग कम से कम महत्वपूर्ण heading के लिए किया जाता है।
   </p> 
   <h3> Type of html heading</h3>
   <textarea name="" id="" cols="100" rows="10">
    1.<h1>.......</h1>
    2.<h2>........</h2>
    3.<h3>.........</h3>
    4.<h4>..........</h4>
    5.<h5>...............</h5>
    6.<h6>................</h6>  
</textarea>
<h3> Example</h3>
<textarea name="" id="" cols="100" rows="15">
