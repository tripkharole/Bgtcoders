      जावा को सन माइक्रोसिस्टम्स (जो अब ओरेकल की सहायक कंपनी है) द्वारा वर्ष 1995 में विकसित किया गया था। जेम्स गोस्लिंग को जावा के पिता के रूप में जाना जाता है। जावा से पहले इसका नाम ओक था । चूंकि ओक पहले से ही एक पंजीकृत कंपनी थी, 
