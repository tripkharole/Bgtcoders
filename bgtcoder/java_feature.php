<h1>Feature of Java </h1>
 <h2>1:-Object-Oriented:-</h2>
<p>जावा एक pure Object-Oriented Programmin language है अर्थात इसमें procedures का प्रयोग नही किया जाता है बल्कि यह सिर्फ objects पर आधारित language है।</P>

<h2>2:-Platform Independent:-</h2>
<p>Java Platform Independent language है अर्थात यह हर किसी प्लेटफार्म में run हो सकती है। यह feature और किसी अन्य language को इतना suit नही करता जितना कि java को करता है।</p>

<p>Java platform independent 
language इसलिए है क्योंकि Java में source Code को Intermediate code में compile किया जाता है जिसे हम byte Code कहते है और वह प्रत्येक System जिसमें JVM होता है वह byte code को interpret कर लेता है। </P>
<center><img src="feature.jpg" height="20%" width="20%"></center>
<h2>3:-Secure:-</h2>
<p> Java का तीसरा बड़ा feature यह है कि यह एक सुरक्षित Language है। Java सबसे अधिक secure है क्योंकि Java Program java runtime environment में run होते है। Java public key encryption का प्रयोग करता है तथा Internet में Java के applications सुरक्षित encrypted रूप में access होते है। </p>
<h2>4:-Compiled तथा Interpreted:-</h2>
<p>Java में source Code को byte Code में compiled किया जाता है तथा फिर उसे JVM के द्वारा machine कोड में interpreted किया जाता है।</p>
<center><img src="interpreter.jpg" height="20%" width="20%"></center>
<h2>5:-Simple, Small तथा Familiar:-</h2>
<p>Java एक Easy Language है क्योंकि इसमें c++ की तरह ही syntax होते है जो कि आसानी से सीखे जा सकते है। जावा में operator  overloading(ऑपरेटर ओवरलोडिंग) तथा header files का प्रयोग नही किया जाता है जिससे यह और भी आसान हो जाती है।</p>
<h2>6:-Portable:-</h2>
<p>Java एक portable Language है क्योंकि byte Code हर किसी System में run हो जाता है इसलिए यह एक portable Language है; इसे आसानी से प्राप्त किया जा सकता है।</p>
<h2>7:-Robust:-</h2>
<p> Java में garbage collection अपने आप हो जाता है इसमें Memory Allocation बहुत बढ़िया है।
           Java में जो भी errors आती है उन्हें आसानी से हल किया जा सकता है। इन्ही सभी कारणों से Java Robust Language है।</p>
<h2>8:-Distributed:-</h2>
<p>Java एक distributed Language है जिसका अर्थ है कि Java Program Internet में run करने के लिए Design किये जाते है तथा Java  में HTTP तथा FTP Protocal का प्रयोग किया जाता है जिससे कि आसानी से Internet में Access किया जा सकें।</p>
<h2>9:-Multi threaded:-</h2>
<p> Java में multi threading का use  किया जाता है अर्थात Java में Program को छोटे sub program में divide किया जाता है तथा इन sub programs को क्रमानुसार execute किया जाता है।</p>
