<h1> HTML HISTORY </h1>
<ul>
<li>
HTML language को Tim Berners lee ने 1991 में develop किया था | अगर हम बात करे इसके Versions के बारे मे तो अभी तक html के कुल 7 version रिलीज़ हो चुके है |
</li>
<h3>HTML 1.0</h3>
<li>यह HTML का पहला version था और इस language को बहुत कम लोग use करते थे |</li>
<h3>HTML 2.0</h3>
<li> इस version में HTML 1.0 के सभी features थे। और साथ मे कुछ extra feature डाले गये थे ताकि website और अच्छे से बनाया जा सके | इस version को 1995 लाया गया |</li>
<h3>HTML 3.0</h3>
<li>यह version आने के बाद html बहुत ही पापुलर हो गया लेकिन इस version मे कुछ compatibility problem होने के कारण इस version को रोक दिया गया लेकिन problem solve होने के बाद इसे फिर से introduce किया गया |</li>
<h3>HTML 3.2</h3>
 <li> इस version मे कुछ नए tags add किये गए। और इसी version के साथ website development मे काफी बढोतरी हुयी | इस version को 1997 मे लाया गया |</li>
<h3>HTML 4.01</h3>
<li> इस version में cascading style sheet के साथ साथ कुछ नए tag भी add किये गये | इस version को 1999 मे लाया गया |</li>
<h3>XHTML</h3>
<li> इस version में XML को भी add किया गया था। इस version को 2000 मे लाया गया |</li>
<h3>HTML 5.0</h3>
 <li> यह HTML का latest version है। इस version मे multimedia support के साथ साथ कुछ नए tags और feature add किये गये है | इस version को 2014 मे लाया गया |</li>
</ul>
