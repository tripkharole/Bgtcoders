<p>5. JavaScript के code को कोई भी जानकार hacker आसानी से hack कर सकता है। जिसके कारण इसमें security (सुरक्षा) की कमी होती है।</p>

<p>6. आजकल के Browser तो इसे Sport करते हैं परंतु पुराने browsers इसे Sport नहीं करते।</p>

<p>7. यह केवल single  inheritance को ही sport करता है। यह multiple inheritance को sport नही करता।</p>
<p>8. अगर JavaScript Code में कोई एक error आ जाये तो इससे पूरी Website काम करना बंद कर सकती है।</p>
