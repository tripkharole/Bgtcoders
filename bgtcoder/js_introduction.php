<p>11.वर्तमान समय में 97% से भी ज्यादा Website Javascript  का Use करती है। Javascript को scripting Language भी कहा जाता है।</p>
<p>12.Javascript का use User की Information जैसे:-  IP address, browser की details आदि को एकत्रित करने के लिए भी किया जाता है।</p>
<h1>Application of JavaScript</h1>
<p>इसका Use बहुत सारीं जगहों पर किया जाता है-</p>
<h2>1. Website बनाने के लिए –:</h2>
<p>JavaScript का Use Website को develop करने के लिए किया जाता है।  इसका Use करके programmer  Website को गतिशील (dynamic) और interactive बनाता है।</p>

<p>Website में इसका Use ज्यादातर validation के लिए किया जाता है।

कुछ famous Website हैं जिनमें Javascript का Use किया गया है – Google, YouTube, Facebook, Amazon, Twitter, और Flipkart आदि।</p>
<h2>2. Web application बनाने के लिए -:</h2>
<p>javascript का Use Computer में चलने वाली web applications को बनाने के लिए किया जाता है।

इसमें web application को बनाने के लिए बहुत सारें framework का Use किया जाता है जैसे कि- React native, React और angular आदि।

आपने google maps देखा होगा। इसे भी  का Use करके बनाया गया है।</p>

<h2>3. Presentation के लिए -:</h2>
<p>JavaScript का Use presentation तैयार करने के लिए भी किया जाता है। Javascript Language में scaling, animated bullet lists और syntax highlighting जैसे Feature शामिल होते है। जिसकी help से कोई भी user Easily से presentation को तैयार कर सकता है।</p>

<p>इसमें सुंदर persentation बनाने के लिए Reveal Js और Bespoke Js लाइब्रेरी का Use किया जाता है। अगर किसी व्यक्ति को programming नहीं भी आती तो भी वह इन लाइब्रेरी का Use करके persentation बना सकता है।</p>

<h2>4.Server Applications को बनाने के लिए -:</h2>
<p>इसका Use server side Application को बनाने के लिए भी करते हैं। इसके लिए इसमें node. js का प्रयोग किया जाता है।</p>

