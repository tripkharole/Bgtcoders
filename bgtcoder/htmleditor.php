    <h1>HTML EDITORS </h1>

    <p> HTML editor एक type का software or tool है जिसके through से आप html की coding easily कर पाते हैं। यह एक simple  software होता है जिसका use आप notepad या text editor की तरह भी कर सकते हैं।
        website or किसी web documents को बनाने के लिए हमें HTML code लिखना पड़ता है और इसी work  को ध्यान में रखकर कई सारे Html editor  बनाये गये हैं जो की हमारे द्वारा लिखे गये code को easily  manage करते हैं और work  को easy बनाने के लिए कई सारे features भी provide करते हैं।
         ये editors सिर्फ HTML ही नही बल्कि अन्य technologies जैसे CSS, javascript, jQuery, XML आदि की कोडिंग में भी काम आते हैं। कुछ html editor ऐसे भी हैं जिनमे आप web development में use होने वाले कई सारे programming languages जैसे: PHP, python, Java आदि की coding भी कर सकते हैं।</p>

         <h3>  TYPE OF HTML EDITORS</h3>
<ul>
HTML EDITORS के दो प्रकार होते हैं:
<li>
1. Text-based Editors:  इस प्रकार के editors नोटपैड की तरह बहुत ही simple होते हैं जिसमें syntax highlighter होते हैं और कुछ प्रोग्राम में auto correction जैसे फीचर भी होते हैं।

ऐसे EDTITOर की खासियत यह है कि यह बहुत ही light weight होते हैं और इसका इंटरफेस बहुत ही simple होता है।
</li>
<li>
2. WYSIWYG Editors: इस तरह के एडिटर text editor से ज्यादा advanced होते हैं। WYSIWYG का फुल फॉर्म “What You See is What You Get” है जो कि ऐसा interface provide करते हैं जहां आप किसी इमेज एडिटर की तरह डिजाइन बनाते हैं और फिर बाद में वह डिजाइन एचटीएमएल कोड में convert हो जाता है।
</li>     
</ul>

<h3> ADVANTAGES OF  HTML EDITORS</h3>
<ul>
<li>
एचटीएमएल एडिटर में कोड बहुत ही जल्दी से लिखा जा सकता है।</li>

<li>
इसमें कोड को ऑर्गेनाइज करना आसान हो जाता </li>
<li>
HTML tags कोई है automatically detect कर लेता है और गलती होने पर error पता चल जाता है।</li>
<li>
Auto complete function से कोडिंग स्पीड बढ़ जाती है।</li>
<li>
WYSIWYG editor से आसानी से बिना कोड लिखें काम किया जा सकता है।</li>
<li>
एडिटर का उपयोग कर आसानी से HTML सीखा जा सकता है।</li>
<li>
एक साथ कई सारे फाइल्स को मैनेज किया जा सकता है।</li>
<li>
अलग-अलग प्रकार के प्रोग्रामिंग लैंग्वेज में प्रोग्राम बनाया जा सकता है।</li>
<li>
कुछ एडिटर्स में एफटीपी की सुविधा भी होती हैं जिससे आप सीधे अपने वेबसाइट में फाइल्स को अपलोड कर सकते हैं।</li>
</ul>

