    <h1> HTML INTRODUCTION</h1> 
    <ul>
        <li>HTML का पूरा नाम Hyper Text Markup Language (हाइपर टेक्स्ट मार्कअप लैंग्वेज) है। </li>
        <li>HTML एक markup Language है जिसका use web pages को बनाने के लिए किया जाता है. इसके अलावा इसका use web apllication बनाने के लिए भी किया जाता है.</li>
        <li>दूसरे शब्दों में कहें तो, “HTML एक कंप्यूटर भाषा है जिसका use website और apllication  बनाने के लिए किया जाता है.”</li>
        <li> HTML बहुत ही easy language है, इसे हम बहुत कम time में easily से सीख सकते हैं.</li>
        <li>HTML वेब पेज के स्ट्रक्चर को describe (परिभाषित) करता है.</li>
        <li>HTML में बहुत सारें tags होते हैं जिनका इस्तेमाल करके हम अपने वेब पेज को आसानी से create कर सकते हैं.</li>
        <li>एचटीएमएल वेब ब्राउज़र को यह बताता है कि वेब पेज में मौजूद content को किस प्रकार दिखाना है.</li>
        <li>HTML में हम CSS का इस्तेमाल करके आकर्षक और सुंदर वेबसाइट बना सकते हैं और इसमें हम जावास्क्रिप्ट का इस्तेमाल भी कर सकते हैं.</li>
        <li>HTML Code को लिखने के लिए किसी विशेष सॉफ्टवेयर की जरूरत नही होती है इसके लिए हमें notepad और web browser की जरूरत होती है जो कि हमारे कंप्यूटर पर पहले से ही मौजूद होते है।</li>
        <li>एचटीएमएल को 1991 में Tim Berners-Lee ने विकसित किया था.</li>
        <li>HTML का इस्तेमाल सबसे ज्यादा किया जाता है इसलिए यह सबसे प्रसिद्ध लैंग्वेज है.</li>
        <li> इस भाषा को एक प्रोग्रामिंग लैंग्वेज नहीं माना जाता, क्योकि यह dynamic web page डिज़ाइन करने में सक्ष्म नहीं होती। इसका उपयोग करके हम केवल static web pages को ही बना सकते है।</li>
        <li> इस भाषा का उपयोग करके दो या दो से अधिक web pages को आपस में जोड़ा जा सकता है।</li>
        <li> HTML को Hypertext और Markup language को आपस में मिलाकर बनाया गया है।</li>
        <li> एचटीएमएल का फ़ाइल extension .html तथा .htm होता है। अर्थात् इसकी फ़ाइलों को .html और .htm लगाकर save किया जाता है.</li>
    </ul>
<h1>FEATURES OF HTML</h1>
<ol>
<li> HTML बहुत ही आसान भाषा है इसलिए इसको सीखना और याद रखना आसान होता है. इसके अलावा इसको हम आसानी से modify भी कर सकते हैं.</li>
<li>इस भाषा का syntax सरल होता है. </li>
<li>यह भाषा platform independent होती है, जिसका मतलब यह है कि इसका इस्तेमाल किसी भी ऑपरेटिंग सिस्टम के साथ किया जा सकता है</li>
<li>इस भाषा के द्वारा हम वेबसाइट में audio, video और image को डाल सकते हैं.</li>
<li>यह case-sensitive नही होती इसलिए इसमें tags को uppercase और lowercase दोनों तरीकों से लिखा जा सकता हैं</li>
<li>HTML में बहुत से formatting tags होते है जिसकी मदद से effective presentation बनाए जा सकते है।</li>
<li> यह एक markup language है जिसमे वेब पेज को डिज़ाइन करना आसान होता है। </li>
<li>इस भाषा का उपयोग करके एक प्रोग्रामर web page पर link जोड़ सकता है। </li>
</ol>
